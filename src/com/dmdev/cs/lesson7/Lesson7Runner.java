package com.dmdev.cs.lesson7;

public class Lesson7Runner {

    public static void main(String[] args) {
        System.out.println(25); // int = default in Java
        System.out.println(25L); // long (long — целое число от -2^63 до 2^63 - 1.)

        System.out.println(23.879); // double = default in Java = 8 bytes
        System.out.println(23.879F); // float = 4 bytes

        System.out.println('?'); // char - symbol
        System.out.println("?..*/"); // String is a class, not a primitive data type

        System.out.println(false); // boolean
    }
}
